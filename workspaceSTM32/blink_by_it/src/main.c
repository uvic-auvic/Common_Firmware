// ----- Timing definitions -------------------------------------------------

// Keep the LED on for 2/3 of a second.
#define BLINK_ON_TICKS  (TIMER_FREQUENCY_HZ * 3 / 4)
#define BLINK_OFF_TICKS (TIMER_FREQUENCY_HZ - BLINK_ON_TICKS)

// ----- main() ---------------------------------------------------------------

// Sample pragmas to cope with warnings. Please note the related line at
// the end of this function, used to pop the compiler diagnostics status.
#pragma GCC diagnostic push
#pragma GCC diagnostic ignored "-Wunused-parameter"
#pragma GCC diagnostic ignored "-Wmissing-declarations"
#pragma GCC diagnostic ignored "-Wreturn-type"

int
main(int argc, char* argv[])
{

  //Initialize two of the GPIO channel in use (C9, A6)
  blink_led_init();

  //Initialize Timer 2 to blink the C9 to show system is okay
  timer2_it_config();

  // Infinite loop
  while (1)
    {
	  continue;
    }
  // Infinite loop, never return.
}

//Timer2 interrupt used to toggle the LED
void TIM2_IRQHandler(void)
{
	if (TIM_GetITStatus(TIM2, TIM_IT_Update) != RESET)
	{
		TIM_ClearITPendingBit(TIM2, TIM_IT_Update);
		//Switch the LED between its last state
		GPIO_WriteBit(GPIOC, GPIO_Pin_9, !GPIO_ReadInputDataBit(GPIOC, GPIO_Pin_9));
	}
}

#pragma GCC diagnostic pop

